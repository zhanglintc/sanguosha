const char* Card_GetNameString(int32_t card)
{
    int category = 0;
    int attribute = 0;
    const char* ret = NULL;
    
    category = CARD_GET_CATEGORY(card);
    attribute = CARD_GET_ATTRIBUTE(card);
    
    switch (category)
    {
        case CATEGORY_BASIC:
            if (attribute != 0)
                ret = szBasic[CARD_GET_ID(card) + attribute + 3];
            else
                ret = szBasic[CARD_GET_ID(card)];
            break;
            
        case CATEGORY_EQUIPMENT:
            ret = szEquipment[CARD_GET_ID(card)];
            break;
            
        case CATEGORY_SPECIAL:
            ret = szSpecial[CARD_GET_ID(card)];
            break;
            
        default:
            break;
    }
    
    return ret;
}

