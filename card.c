int32_t Card_Make(int32_t suit, int32_t rank, int32_t category, int32_t attribute, int32_t cid)
{
    int32_t card = 0;
    
    card |= (suit & 0xF0);
    card |= (rank & 0xF);
    card |= ((category << 30) & 0xC0000000);
    card |= ((attribute << 27) & 0x38000000);
    card |= ((cid << 8) & 0x07FFFF00);
    
    return card;
}

